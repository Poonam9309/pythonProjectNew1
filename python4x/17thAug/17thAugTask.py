# 1. Difference between = and == operators in Python
# The = operator is used for assigning values to variables. e.g: x = 5 assigns the value 5 to the variable x.
# The == operator is used for comparing if two values are equal. e.g: x == 5 checks if x is equal to 5 and returns True or False.

